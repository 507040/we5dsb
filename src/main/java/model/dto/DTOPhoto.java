package model.dto;

public class DTOPhoto {
	private int pno;
	private int bno;
	private String file_r;
	private String file_o;
	private String file_s;
	public int getPno() {
		return pno;
	}
	public void setPno(int pno) {
		this.pno = pno;
	}
	public int getBno() {
		return bno;
	}
	public void setBno(int bno) {
		this.bno = bno;
	}
	public String getFile_r() {
		return file_r;
	}
	public void setFile_r(String file_r) {
		this.file_r = file_r;
	}
	public String getFile_o() {
		return file_o;
	}
	public void setFile_o(String file_o) {
		this.file_o = file_o;
	}
	public String getFile_s() {
		return file_s;
	}
	public void setFile_s(String file_s) {
		this.file_s = file_s;
	}
	
	
}

